const now = new Date()

function daysFromNow(days: number, hours = 11, minutes = 0): string {
  const d = new Date(now)
  d.setDate(d.getDate() + days)
  d.setHours(hours, minutes, 0, 0)
  return d.toISOString()
}

function deadlineFromNow(days: number, hours = 10, minutes = 0): string {
  const d = new Date(now)
  d.setDate(d.getDate() + days)
  d.setHours(hours, minutes, 0, 0)
  return d.toISOString()
}

